class Job{
    private String role;
    private int id;
    private double salary;

    public String getRole(){
        return role;
    }

    public void setRole(String role){
        this.role = role;
    }

    public int getId(){
        return id;
    }

    public void setId(int id){
        this.id=id;
    }

    public double getSalary(){
        return salary;
    }

    public void setSalary(double salary){
        this.salary=salary;
    }
}